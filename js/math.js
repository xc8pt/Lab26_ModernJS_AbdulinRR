export function square(a, h) {
    const S = 1/2 * a * h;
    return `Площадь треуголника при a = ${a} и h = ${h}\nРовна: ${S}`;
}
export function cube(a) {
    const V = a * 3;
    return `Объем куба с длинной ребра a = ${a}\nРовна: ${V}`;
}
